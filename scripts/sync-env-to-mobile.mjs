import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDirectory, "..");
const webEnvPath = path.join(repoRoot, "apps", "web", ".env");
const mobileEnvPath = path.join(repoRoot, "apps", "mobile", ".env");
const productionWebApiBaseUrl = "https://gigeze.example";

const sourceKeys = {
  supabaseUrl: ["SUPABASE_URL", "NEXT_PUBLIC_SUPABASE_URL"],
  supabaseAnonKey: ["SUPABASE_ANON_KEY", "NEXT_PUBLIC_SUPABASE_ANON_KEY"],
  webApiBaseUrl: ["EXPO_PUBLIC_WEB_API_URL", "MOBILE_WEB_API_BASE_URL", "NEXT_PUBLIC_SITE_URL"],
};

function parseEnv(contents) {
  const values = new Map();

  for (const rawLine of contents.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }

    const normalizedLine = line.startsWith("export ") ? line.slice("export ".length).trim() : line;
    const separatorIndex = normalizedLine.indexOf("=");
    if (separatorIndex === -1) {
      continue;
    }

    const key = normalizedLine.slice(0, separatorIndex).trim();
    const rawValue = normalizedLine.slice(separatorIndex + 1).trim();
    values.set(key, unquoteEnvValue(rawValue));
  }

  return values;
}

function unquoteEnvValue(value) {
  if (value.length < 2) {
    return value;
  }

  const first = value[0];
  const last = value[value.length - 1];
  if ((first === `"` && last === `"`) || (first === `'` && last === `'`)) {
    return value.slice(1, -1);
  }

  return value;
}

function quoteEnvValue(value) {
  if (!/[#\s"'\\]/.test(value)) {
    return value;
  }

  return JSON.stringify(value);
}

function pickRequiredValue(values, names, targetName) {
  for (const name of names) {
    const value = values.get(name);
    if (value) {
      return { sourceName: name, value };
    }
  }

  throw new Error(`Missing ${targetName}. Expected one of: ${names.join(", ")}`);
}

function pickOptionalValue(values, names) {
  for (const name of names) {
    const value = values.get(name);
    if (value) {
      return { sourceName: name, value };
    }
  }

  return null;
}

async function main() {
  const webEnv = await readFile(webEnvPath, "utf8").catch((error) => {
    if (error && error.code === "ENOENT") {
      throw new Error(`Cannot find ${webEnvPath}. Create apps/web/.env first.`);
    }

    throw error;
  });
  const values = parseEnv(webEnv);
  const mobileEnv = await readFile(mobileEnvPath, "utf8").catch((error) => {
    if (error && error.code === "ENOENT") {
      return "";
    }

    throw error;
  });
  const mobileValues = parseEnv(mobileEnv);

  const supabaseUrl = pickRequiredValue(values, sourceKeys.supabaseUrl, "Supabase URL");
  const supabaseAnonKey = pickRequiredValue(values, sourceKeys.supabaseAnonKey, "Supabase anon key");
  const webApiBaseUrl =
    pickOptionalValue(values, sourceKeys.webApiBaseUrl) ??
    pickOptionalValue(mobileValues, ["EXPO_PUBLIC_WEB_API_URL"]) ??
    { sourceName: "production default", value: productionWebApiBaseUrl };

  const output = [
    "# Generated by npm run env:sync:mobile",
    "# Source: apps/web/.env",
    `EXPO_PUBLIC_SUPABASE_URL=${quoteEnvValue(supabaseUrl.value)}`,
    `EXPO_PUBLIC_SUPABASE_ANON_KEY=${quoteEnvValue(supabaseAnonKey.value)}`,
    `EXPO_PUBLIC_WEB_API_URL=${quoteEnvValue(webApiBaseUrl.value)}`,
    "",
  ].join("\n");

  await mkdir(path.dirname(mobileEnvPath), { recursive: true });
  await writeFile(mobileEnvPath, output, "utf8");

  console.log(`Wrote ${path.relative(repoRoot, mobileEnvPath)}`);
  console.log(`Mapped ${supabaseUrl.sourceName} -> EXPO_PUBLIC_SUPABASE_URL`);
  console.log(`Mapped ${supabaseAnonKey.sourceName} -> EXPO_PUBLIC_SUPABASE_ANON_KEY`);
  console.log(`Mapped ${webApiBaseUrl.sourceName} -> EXPO_PUBLIC_WEB_API_URL`);
  if (webApiBaseUrl.sourceName === "production default") {
    console.log(`Defaulted EXPO_PUBLIC_WEB_API_URL to ${productionWebApiBaseUrl}`);
  }
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exitCode = 1;
});
