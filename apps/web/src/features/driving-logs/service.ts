type DrivingLogDistanceSource = {
  startOdometer: number;
  endOdometer: number;
  businessKm: number;
  personalKm: number;
  purpose?: string | null;
  gpsSamples?: Array<{
    latitude: number | { toNumber(): number };
    longitude: number | { toNumber(): number };
    recordedAt?: Date;
  }>;
};

function toCoordinateNumber(value: number | { toNumber(): number }) {
  return typeof value === "number" ? value : value.toNumber();
}

const weakGpsLocationLabels = new Set([
  "gps route recorded",
  "gps trip start",
  "gps trip end",
  "unknown start",
  "unknown end",
]);

function isWeakGpsLocationLabel(value?: string | null) {
  const normalized = value?.trim().toLowerCase();
  return !normalized || weakGpsLocationLabels.has(normalized);
}

export function getDrivingLogComputedDistanceKm(log: DrivingLogDistanceSource) {
  const odometerDistanceKm = Math.max(0, log.endOdometer - log.startOdometer);
  const splitDistanceKm = Math.max(0, log.businessKm + log.personalKm);
  const gpsDistanceKm =
    log.gpsSamples && log.gpsSamples.length >= 2
      ? calculateTripDistanceKm(
          log.gpsSamples.map((sample, index) => ({
            latitude: toCoordinateNumber(sample.latitude),
            longitude: toCoordinateNumber(sample.longitude),
            recordedAt: sample.recordedAt?.toISOString() ?? `sample-${index}`,
            accuracyMeters: null,
          })),
        )
      : 0;

  return Math.max(odometerDistanceKm, splitDistanceKm, gpsDistanceKm);
}

function mapGpsSampleCreateManyData(drivingLogId: string, gpsSamples: DrivingLogGpsSampleInput[]) {
  return gpsSamples.map((sample, index) => ({
    drivingLogId,
    sampleIndex: index,
    latitude: sample.latitude,
    longitude: sample.longitude,
    accuracyMeters: sample.accuracyMeters,
    recordedAt: sample.recordedAt,
  }));
}

