export async function createDrivingLogWithGpsSamples(
  input: DrivingLogCreateInput,
  gpsSamples: DrivingLogGpsSampleInput[],
  context: DrivingLogServiceContext,
) {
  await assertJourneyInWorkspace(context.workspaceId, input.journeyId || undefined);
  await assertVehicleForTripMode(context.workspaceId, input.tripMode, input.vehicleId || undefined);

  return prisma.$transaction(async (tx) => {
    const draft = await tx.drivingLog.create({
      data: {
        workspaceId: context.workspaceId,
        createdByUserId: context.userId,
        journeyId: input.journeyId || undefined,
        tripMode: input.tripMode,
        vehicleId: input.vehicleId || undefined,
        date: input.date,
        startTime: input.startTime,
        endTime: input.endTime,
        startLocation: input.startLocation,
        endLocation: input.endLocation,
        ...mapDrivingLogGeocodeData(input),
        startOdometer: input.startOdometer,
        endOdometer: input.endOdometer,
        businessKm: input.businessKm,
        personalKm: input.personalKm,
        purpose: input.purpose,
        hasRouteSamples: input.hasRouteSamples ?? gpsSamples.length > 0,
        notes: input.notes,
      },
    });

    if (gpsSamples.length > 0) {
      await tx.drivingLogGpsSample.createMany({
        data: mapGpsSampleCreateManyData(draft.id, gpsSamples),
      });
    }

    return draft;
  });
}

export async function appendDrivingLogGpsSamples(
  logId: string,
  gpsSamples: DrivingLogGpsSampleInput[],
  context: DrivingLogServiceContext,
) {
  if (gpsSamples.length === 0) {
    return { count: 0 };
  }

  const existing = await prisma.drivingLog.findFirst({
    where: {
      id: logId,
      workspaceId: context.workspaceId,
      createdByUserId: context.userId,
      deletedAt: null,
    },
    select: { id: true },
  });

  if (!existing) {
    throw new AppError("driving-log-not-found", "DRIVING_LOG_NOT_FOUND");
  }

  const result = await prisma.drivingLogGpsSample.createMany({
    data: mapGpsSampleCreateManyData(logId, gpsSamples),
    skipDuplicates: true,
  });

  await prisma.drivingLog.update({
    where: { id: logId },
    data: { hasRouteSamples: true },
  });

  return result;
}

