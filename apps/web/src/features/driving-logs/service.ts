async function assertJourneyInWorkspace(workspaceId: string, journeyId?: string) {
  if (!journeyId) {
    return;
  }

  const Tour = await prisma.tour.findFirst({
    where: { id: journeyId, workspaceId },
    select: { id: true },
  });

  if (!Tour) {
    throw new AppError("driving-log-invalid-Tour-reference", "DRIVING_LOG_INVALID_JOURNEY_REFERENCE");
  }
}

async function assertVehicleForTripMode(workspaceId: string, tripMode: TripMode, vehicleId?: string) {
  if (tripMode === "WALK") {
    if (vehicleId) {
      throw new AppError("driving-log-walk-trip-does-not-allow-vehicle", "DRIVING_LOG_WALK_TRIP_DOES_NOT_ALLOW_VEHICLE");
    }

    return null;
  }

  if (!vehicleId) {
    return null;
  }

  const vehicle = await prisma.vehicle.findFirst({
    where: { id: vehicleId, workspaceId },
    select: { id: true, vehicleMode: true, enableBusinessSplit: true } as never,
  }) as { id: string; vehicleMode: VehicleMode; enableBusinessSplit: boolean } | null;

  if (!vehicle) {
    throw new AppError("driving-log-invalid-vehicle-reference", "DRIVING_LOG_INVALID_VEHICLE_REFERENCE");
  }

  if (vehicle.vehicleMode !== tripMode) {
    throw new AppError("driving-log-vehicle-mode-mismatch", "DRIVING_LOG_VEHICLE_MODE_MISMATCH");
  }

  return vehicle;
}

function normalizeDrivingLogSplit(input: {
  tripMode: TripMode;
  startOdometer: number;
  endOdometer: number;
  businessKm: number;
  personalKm: number;
}, options: { enableBusinessSplit: boolean }) {
  const totalDistanceKm = Math.max(0, input.endOdometer - input.startOdometer);

  if (input.tripMode === "WALK" || !options.enableBusinessSplit) {
    return {
      businessKm: 0,
      personalKm: totalDistanceKm,
    };
  }

  return {
    businessKm: input.businessKm,
    personalKm: input.personalKm,
  };
}

function normalizeDrivingLogMetadataSplit(input: {
  tripMode: TripMode;
  startOdometer?: number;
  endOdometer?: number;
  businessKm: number;
  personalKm: number;
}, options: { enableBusinessSplit: boolean }) {
  if (input.tripMode === "WALK" || !options.enableBusinessSplit) {
    const hasOdometers = typeof input.startOdometer === "number" && typeof input.endOdometer === "number";
    const startOdometer = typeof input.startOdometer === "number" ? input.startOdometer : 0;
    const endOdometer = typeof input.endOdometer === "number" ? input.endOdometer : 0;
    const totalDistanceKm = hasOdometers
      ? Math.max(0, endOdometer - startOdometer)
      : Math.max(0, input.businessKm + input.personalKm);

    return {
      businessKm: 0,
      personalKm: totalDistanceKm,
    };
  }

  return {
    businessKm: input.businessKm,
    personalKm: input.personalKm,
  };
}

export async function listDrivingLogs(workspaceId: string) {
  const logs = await prisma.drivingLog.findMany({
    where: { workspaceId, deletedAt: null },
    include: {
      Tour: {
        select: { id: true, title: true, slug: true },
      },
      vehicle: {
        select: { id: true, name: true, enableBusinessSplit: true },
      },
      gpsSamples: {
        select: {
          latitude: true,
          longitude: true,
          recordedAt: true,
        },
        orderBy: [{ sampleIndex: "asc" }, { recordedAt: "asc" }],
      },
    },
    orderBy: { date: "desc" },
  });

  return logs.map((log) => ({
    ...log,
    computedDistanceKm: getDrivingLogComputedDistanceKm(log),
  }));
}

export async function listDrivingLogsPage(
  workspaceId: string,
  page: number,
  pageSize: number,
  filters: DrivingLogListFilters = {},
): Promise<DrivingLogListPage> {
  const safePage = Math.max(1, Math.floor(page));
  const safePageSize = Math.max(1, Math.floor(pageSize));
  const skip = (safePage - 1) * safePageSize;
  const where = {
    workspaceId,
    deletedAt: null,
    ...(filters.from || filters.to
      ? {
          date: {
            ...(filters.from ? { gte: filters.from } : {}),
            ...(filters.to ? { lte: filters.to } : {}),
          },
        }
      : {}),
    ...(filters.vehicleId ? { vehicleId: filters.vehicleId } : {}),
    ...(filters.journeyId ? { journeyId: filters.journeyId } : {}),
    ...(filters.autoGeneratedOnly
      ? {
          notes: {
            contains: "[Trip draft] Passive GPS session captured.",
          },
        }
      : {}),
  };

  const [logs, totalCount] = await prisma.$transaction([
    prisma.drivingLog.findMany({
      where,
      include: {
        Tour: {
          select: { id: true, title: true, slug: true },
        },
        vehicle: {
          select: { id: true, name: true, enableBusinessSplit: true },
        },
        gpsSamples: {
          select: {
            latitude: true,
            longitude: true,
            recordedAt: true,
          },
          orderBy: [{ sampleIndex: "asc" }, { recordedAt: "asc" }],
        },
      },
      orderBy: [{ date: "desc" }, { createdAt: "desc" }],
      skip,
      take: safePageSize,
    }),
    prisma.drivingLog.count({
      where,
    }),
  ]);

  return {
    logs: logs.map((log) => ({
      ...log,
      computedDistanceKm: getDrivingLogComputedDistanceKm(log),
    })),
    totalCount,
  };
}

