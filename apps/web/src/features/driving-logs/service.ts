export async function getStartOdometerForVehicle(workspaceId: string, vehicleId?: string) {
  // First, try to get the latest driving log's end odometer
  const latest = await prisma.drivingLog.findFirst({
    where: {
      workspaceId,
      deletedAt: null,
      ...(vehicleId ? { vehicleId } : {}),
    },
    orderBy: { date: "desc" },
    select: { endOdometer: true },
  });

  if (latest) {
    return latest.endOdometer;
  }

  // If no previous log and we have a vehicle ID, use the vehicle's starting odometer
  if (vehicleId) {
    const vehicle = await prisma.vehicle.findFirst({
      where: { id: vehicleId, workspaceId },
      select: { startingOdometer: true },
    });

    if (vehicle) {
      return vehicle.startingOdometer;
    }
  }

  // Fallback to 0
  return 0;
}

