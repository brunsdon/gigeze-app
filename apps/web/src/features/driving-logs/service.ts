export async function createDrivingLog(input: DrivingLogCreateInput, context: DrivingLogServiceContext) {
  await assertJourneyInWorkspace(context.workspaceId, input.journeyId || undefined);
  const vehicle = await assertVehicleForTripMode(context.workspaceId, input.tripMode, input.vehicleId || undefined);
  const split = normalizeDrivingLogSplit(input, { enableBusinessSplit: vehicle?.enableBusinessSplit === true });

  return prisma.drivingLog.create({
    data: {
      workspaceId: context.workspaceId,
      createdByUserId: context.userId,
      journeyId: input.journeyId || undefined,
      tripMode: input.tripMode,
      vehicleId: input.vehicleId || undefined,
      date: input.date,
      startTime: input.startTime,
      endTime: input.endTime,
      startLocation: input.startLocation,
      endLocation: input.endLocation,
      ...mapDrivingLogGeocodeData(input),
      startOdometer: input.startOdometer,
      endOdometer: input.endOdometer,
      businessKm: split.businessKm,
      personalKm: split.personalKm,
      purpose: input.purpose,
      hasRouteSamples: input.hasRouteSamples ?? false,
      notes: input.notes,
    },
  });
}

