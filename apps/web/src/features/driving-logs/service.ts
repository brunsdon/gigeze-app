export async function refreshDrivingLogGpsEndpointLocations(logId: string, workspaceId: string) {
  const log = await prisma.drivingLog.findFirst({
    where: { id: logId, workspaceId, deletedAt: null },
    select: {
      id: true,
      startLocation: true,
      endLocation: true,
      startFormattedAddress: true,
      endFormattedAddress: true,
      gpsSamples: {
        select: {
          latitude: true,
          longitude: true,
        },
        orderBy: [{ sampleIndex: "asc" }, { recordedAt: "asc" }],
      },
    },
  });

  if (!log || log.gpsSamples.length === 0) {
    return false;
  }

  const shouldRefreshStart = isWeakGpsLocationLabel(log.startFormattedAddress || log.startLocation);
  const shouldRefreshEnd = isWeakGpsLocationLabel(log.endFormattedAddress || log.endLocation);

  if (!shouldRefreshStart && !shouldRefreshEnd) {
    return false;
  }

  const endpoints = await reverseGeocodeTripEndpoints(
    log.gpsSamples.map((sample) => ({
      latitude: toCoordinateNumber(sample.latitude),
      longitude: toCoordinateNumber(sample.longitude),
    })),
  );
  const updateData: {
    startLocation?: string;
    endLocation?: string;
    startFormattedAddress?: string;
    endFormattedAddress?: string;
  } = {};

  if (shouldRefreshStart && endpoints.startLocation) {
    updateData.startLocation = endpoints.startLocation;
    updateData.startFormattedAddress = endpoints.startLocation;
  }

  if (shouldRefreshEnd && endpoints.endLocation) {
    updateData.endLocation = endpoints.endLocation;
    updateData.endFormattedAddress = endpoints.endLocation;
  }

  if (Object.keys(updateData).length === 0) {
    return false;
  }

  await prisma.drivingLog.update({
    where: { id: log.id },
    data: updateData,
  });

  return true;
}

